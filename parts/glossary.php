
<h2>Glossary</h2>

     <dl>

          <div class="glossary--item" id="absolute-zero">
          <dt>Absolute zero</dt> <dd>The lowest possible temperature, at which substances contain no heat energy.</dd>
          </div>

          <div class="glossary--item" id="acceleration">
          <dt>Acceleration</dt> <dd>The rate at which the speed of an object is changing.</dd>
          </div>

          <div class="glossary--item" id="anthropic-principle">
          <dt>Anthropic principle</dt> <dd>The idea that we see the universe the way it is because if it were different, we would not be here to observe it.</dd>
          </div>

          <div class="glossary--item" id="antiparticle">
          <dt>Antiparticle</dt> <dd>Each type of matter particle has a corresponding antiparticle. When a particle collides with its antiparticle, both are annihilated, leaving only energy.</dd>
          </div>

          <div class="glossary--item" id="atom">
          <dt>Atom</dt> <dd>The basic unit of ordinary matter, made up of a tiny nucleus (consisting of protons and neutrons) surrounded by orbiting electrons.</dd>
          </div>

          <div class="glossary--item" id="big-bang">
          <dt>Big bang</dt> <dd>The singularity at the beginning of the universe.</dd>
          </div>

          <div class="glossary--item" id="big-crunch">
          <dt>Big crunch</dt> <dd>The singularity at the end of the universe.</dd>
          </div>

          <div class="glossary--item" id="black-hole">
          <dt>Black hole</dt> <dd>A region of space-time from which nothing, not even light, can escape, because gravity is so strong.</dd>
          </div>

          <div class="glossary--item" id="coordinates">
          <dt>Coordinates</dt> <dd>Numbers that specify the position of a point in space and time.</dd>
          </div>

          <div class="glossary--item" id="cosmological-constant">
          <dt>Cosmological constant</dt> <dd>A mathematical device used by Einstein to give space-time an inbuilt tendency to expand.</dd>
          </div>

          <div class="glossary--item" id="cosmology">
          <dt>Cosmology</dt> <dd>The study of the universe as a whole.</dd>
          </div>

          <div class="glossary--item" id="dark-matter">
          <dt>Dark matter</dt> <dd>Matter in galaxies, clusters, and possibly between clusters that has not been observed directly but can be detected by its gravitational effect. As much as 90 percent of the mass of the universe may be in the form of dark matter.</dd>
          </div>

          <div class="glossary--item" id="duality">
          <dt>Duality</dt> <dd>A correspondence between apparently different theories that lead to the same physical results.</dd>
          </div>

          <div class="glossary--item" id="einstein-rosen-bridge">
          <dt>Einstein-Rosen bridge</dt> <dd>A thin tube of space-time linking two black holes. See also Wormhole.</dd>
          </div>

          <div class="glossary--item" id="electric-charge">
          <dt>Electric charge</dt> <dd>A property of a particle by which it may repel (or attract) other particles that have a charge of similar (or opposite) sign.</dd>
          </div>

          <div class="glossary--item" id="electromagnetic-force">
          <dt>Electromagnetic force</dt> <dd>The force that arises between particles with electric charge; the second strongest of the four fundamental forces.</dd>
          </div>

          <div class="glossary--item" id="electron">
          <dt>Electron</dt> <dd>A particle with negative electric charge that orbits the nucleus of an atom.</dd>
          </div>

          <div class="glossary--item" id="electroweak-unification-energy">
          <dt>Electroweak unification energy</dt> <dd>The energy (around 100 GeV) above which the distinction between the electromagnetic force and the weak force disappears.</dd>
          </div>

          <div class="glossary--item" id="elementary-particle">
          <dt>Elementary particle</dt> <dd>A particle that, it is believed, cannot be subdivided.</dd>
          </div>

          <div class="glossary--item" id="event">
          <dt>Event</dt> <dd>A point in space-time, specified by its time and place.</dd>
          </div>

          <div class="glossary--item" id="event-horizon">
          <dt>Event horizon</dt> <dd>The boundary of a black hole.</dd>
          </div>

          <div class="glossary--item" id="field">
          <dt>Field</dt> <dd>Something that exists throughout space and time, as opposed to a particle that exists at only one point at a time.</dd>
          </div>

          <div class="glossary--item" id="frequency">
          <dt>Frequency</dt> <dd>For a wave, the number of complete cycles per second.</dd>
          </div>

          <div class="glossary--item" id="gamma-rays">
          <dt>Gamma rays</dt> <dd>Electromagnetic rays of very short wavelength, produced in radioactive decay or by collisions of elementary particles.</dd>
          </div>

          <div class="glossary--item" id="general-relativity">
          <dt>General relativity</dt> <dd>Einstein’s theory based on the idea that the laws of science should be the same for all observers, no matter how they are moving. It explains the force of gravity in terms of the curvature of a four-dimensionalspace-time.</dd>
          </div>

          <div class="glossary--item" id="geodesic">
          <dt>Geodesic</dt> <dd>The shortest (or longest) path between two points.</dd>
          </div>

          <div class="glossary--item" id="grand-unified-theory">
          <dt>Grand unified theory (GUT)</dt> <dd>A theory that unifies the electromagnetic, strong, and weak forces.</dd>
          </div>

          <div class="glossary--item" id="light-second-light-year">
          <dt>Light-second (light-year)</dt> <dd>The distance traveled by light in one second (year).</dd>
          </div>

          <div class="glossary--item" id="magnetic-field">
          <dt>Magnetic field</dt> <dd>The field responsible for magnetic forces, now incorporated along with the electric field into the electromagnetic field.</dd>
          </div>

          <div class="glossary--item" id="mass">
          <dt>Mass</dt> <dd>The quantity of matter in a body; its inertia, or resistance to acceleration.</dd>
          </div>

          <div class="glossary--item" id="microwave-background-radiation">
          <dt>Microwave background radiation</dt> <dd>The radiation from the glowing of the hot early universe, now so greatly red-shifted that it appears not as light but as microwaves (radio waves with a wavelength of a few centimeters).</dd>
          </div>

          <div class="glossary--item" id="neutrino">
          <dt>Neutrino</dt> <dd>An extremely light particle that is affected only by the weak force and gravity.</dd>
          </div>

          <div class="glossary--item" id="neutron">
          <dt>Neutron</dt> <dd>A particle very similar to the proton but without charge, which accounts for roughly half the particles in the nuclei of most atoms.</dd>
          </div>

          <div class="glossary--item" id="neutron-star">
          <dt>Neutron star</dt> <dd>The cold star that sometimes remains after a supernova explosion, when the core of material at the center of a star collapses into a dense mass of neutrons.</dd>
          </div>

          <div class="glossary--item" id="no-boundary-condition">
          <dt>No-boundary condition</dt> <dd>The idea that the universe is finite but has no boundary.</dd>
          </div>

          <div class="glossary--item" id="nuclear-fusion">
          <dt>Nuclear fusion</dt> <dd>The process by which two nuclei collide and coalesce to form a single, heavier nucleus.</dd>
          </div>

          <div class="glossary--item" id="nucleus">
          <dt>Nucleus</dt> <dd>The central part of an atom, consisting only of protons and neutrons, held together by the strong force.</dd>
          </div>

          <div class="glossary--item" id="particle-accelerator">
          <dt>Particle accelerator</dt> <dd>A machine that, using electromagnets, can accelerate moving charged particles, giving them more energy.</dd>
          </div>

          <div class="glossary--item" id="phase">
          <dt>Phase</dt> <dd>For a wave, the position in its cycle at a specified time</dt> <dd>a measure of whether it is at a crest, a trough, or somewhere in between.</dd>
          </div>

          <div class="glossary--item" id="photon">
          <dt>Photon</dt> <dd>A quantum of light.</dd>
          </div>

          <div class="glossary--item" id="plancks-quantum-principle">
          <dt>Planck’s quantum principle</dt> <dd>The idea that light (or any other classical waves) can be emitted or absorbed only in discrete quanta, whose energy is proportional to their frequency, and inversely proportional to their wavelength.</dd>
          </div>

          <div class="glossary--item" id="positron">
          <dt>Positron</dt> <dd>The (positively charged) antiparticle of the electron.</dd>
          </div>

          <div class="glossary--item" id="proportional">
          <dt>Proportional</dt> <dd>"X is proportional to Y" means that when Y is multiplied by any number, so is X. "X is inversely proportional to Y" means that when Y is multiplied by any number, X is divided by that number.</dd>
          </div>

          <div class="glossary--item" id="proton">
          <dt>Proton</dt> <dd>A particle very similar to the neutron but positively charged, which accounts for roughly half the particles in the nuclei of most atoms.</dd>
          </div>

          <div class="glossary--item" id="quantum-mechanics">
          <dt>Quantum mechanics</dt> <dd>The theory developed from Planck’s quantum principle and Heisenberg’s uncertainty principle.</dd>
          </div>

          <div class="glossary--item" id="quark">
          <dt>Quark</dt> <dd>A (charged) elementary particle that feels the strong force. Protons and neutrons are each composed of three quarks.</dd>
          </div>

          <div class="glossary--item" id="radar">
          <dt>Radar</dt> <dd>A system using pulsed radio waves to detect the position of objects by measuring the time it takes a single pulse to reach the object and be reflected back.</dd>
          </div>

          <div class="glossary--item" id="radiactivity">
          <dt>Radioactivity</dt> <dd>The spontaneous breakdown of one type of atomic nucleus into another.</dd>
          </div>

          <div class="glossary--item" id="red-shift">
          <dt>Red shift</dt> <dd>The reddening of light from a star that is moving away from us, due to the Doppler effect.</dd>
          </div>

          <div class="glossary--item" id="singularity">
          <dt>Singularity</dt> <dd>A point in space-time at which the space-time curvature (or some other physical quantity) becomes infinite.</dd>
          </div>

          <div class="glossary--item" id="space-time">
          <dt>Space-time</dt> <dd>The four-dimensionalspace whose points are events.</dd>
          </div>

          <div class="glossary--item" id="spatial-dimension">
          <dt>Spatial dimension</dt> <dd>Any of the three dimensions—that is, any dimension except the time dimension.</dd>
          </div>

          <div class="glossary--item" id="special-relativity">
          <dt>Special relativity</dt> <dd>Einstein’s theory based on the idea that the laws of science should be the same for all observers, no matter how they are moving, in the absence of gravitational phenomena.</dd>
          </div>

          <div class="glossary--item" id="spectrum">
          <dt>Spectrum</dt> <dd>The component frequencies that make up a wave. The visible part of the sun’s spectrum can be seen in a rainbow.</dd>
          </div>

          <div class="glossary--item" id="string-theory">
          <dt>String theory</dt> <dd>A theory of physics in which particles are described as waves on strings. Strings have length but no other dimension.</dd>
          </div>

          <div class="glossary--item" id="strong-force">
          <dt>Strong force</dt> <dd>The strongest of the four fundamental forces, with the shortest range of all. It holds the quarks together within protons and neutrons, and holds the protons and neutrons together to form atoms.</dd>
          </div>

          <div class="glossary--item" id="uncertainty-principle">
          <dt>Uncertainty principle</dt> <dd>The principle, formulated by Heisenberg, that it is not possible to be exactly sure of both the position and the velocity of a particle; the more accurately one is known, the less accurately the other can be known.</dd>
          </div>

          <div class="glossary--item" id="virtual-particle">
          <dt>Virtual particle</dt> <dd>In quantum mechanics, a particle that can never be directly detected, but whose existence does have measurable effects.</dd>
          </div>

          <div class="glossary--item" id="wave-particle-duality">
          <dt>Wave/particle duality</dt> <dd>The concept in quantum mechanics that there is no distinction between waves and particles; particles may sometimes behave like waves, and waves like particles.</dd>
          </div>

          <div class="glossary--item" id="wavelength">
          <dt>Wavelength</dt> <dd>For a wave, the distance between two adjacent troughs or two adjacent crests.</dd>
          </div>

          <div class="glossary--item" id="weak-force">
          <dt>Weak force</dt> <dd>The second weakest of the four fundamental forces, after gravity, with a very short range. It affects all matter particles, but not force-carrying particles.</dd>
          </div>

          <div class="glossary--item" id="weight">
          <dt>Weight</dt> <dd>The force exerted on a body by a gravitational field. It is proportional to, but not the same as, its mass.</dd>
          </div>

          <div class="glossary--item" id="wormhole">
          <dt>Wormhole</dt> <dd>A thin tube of space-time connecting distant regions of the universe. Wormholes might also link to parallel or baby universes and could provide the possibility of time travel.</dd>
          </div>

     </dl>